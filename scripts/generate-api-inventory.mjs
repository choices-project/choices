#!/usr/bin/env node
/**
 * Regenerates docs/API/inventory.md from every route.ts under web/app/api/.
 * Run from repo root: node scripts/generate-api-inventory.mjs
 */
import { readFileSync, readdirSync, statSync, writeFileSync } from 'fs';
import { dirname, join, relative } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const apiRoot = join(root, 'web/app/api');
const outPath = join(root, 'docs/API/inventory.md');

const METHODS = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE', 'OPTIONS', 'HEAD'];

function walkRouteFiles(dir, acc = []) {
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) walkRouteFiles(p, acc);
    else if (name === 'route.ts') acc.push(p);
  }
  return acc;
}

function methodsForFile(filePath) {
  const content = readFileSync(filePath, 'utf8');
  const found = [];
  for (const m of METHODS) {
    const re = new RegExp(`export (const|async function|function) ${m}\\b`);
    if (re.test(content)) found.push(m);
  }
  return found.sort();
}

const files = walkRouteFiles(apiRoot).sort((a, b) => relative(apiRoot, a).localeCompare(relative(apiRoot, b)));

const today = new Date().toISOString().slice(0, 10);
const rows = files.map((abs) => {
  const relApi = relative(apiRoot, abs).replace(/\\/g, '/');
  const methods = methodsForFile(abs).join(', ') || '—';
  return `| \`${relApi}\` | ${methods} |`;
});

const md = `# API route inventory (generated)

_Auto-generated by \`node scripts/generate-api-inventory.mjs\`. Last run: ${today}._

This table lists every Next.js App Router handler under \`web/app/api/\` and the HTTP methods it exports (\`export const GET\`, \`export async function POST\`, etc.). It is the companion to the narrative docs in [\`README.md\`](README.md) and [\`contracts.md\`](contracts.md).

**Total route modules:** ${files.length}

| Path (under \`web/app/api/\`) | Methods |
| --- | --- |
${rows.join('\n')}
`;

writeFileSync(outPath, md, 'utf8');
console.log(`Wrote ${relative(root, outPath)} (${files.length} routes).`);
